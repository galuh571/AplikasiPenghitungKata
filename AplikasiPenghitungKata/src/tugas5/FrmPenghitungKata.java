    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        String teks = txtArea.getText().toLowerCase();
        String cari = txtCari.getText().toLowerCase();

        if (cari.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan kata yang ingin dicari!");
            return;
        }

        int count = 0;
        Matcher m = Pattern.compile("\\b" + Pattern.quote(cari) + "\\b").matcher(teks);
        while (m.find()) count++;

        JOptionPane.showMessageDialog(this, 
            "Kata \"" + cari + "\" ditemukan sebanyak " + count + " kali.");
    
    }//GEN-LAST:event_btnCariActionPerformed

