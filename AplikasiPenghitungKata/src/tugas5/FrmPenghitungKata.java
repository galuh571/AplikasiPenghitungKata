    private void hitungOtomatis() {
        String teks = txtArea.getText().trim();
        int karakter = teks.length();
        int kata = teks.isEmpty() ? 0 : teks.split("\\s+").length;
        int kalimat = teks.isEmpty() ? 0 : teks.split("[.!?]+").length;
        int paragraf = teks.isEmpty() ? 0 : teks.split("\\n+").length;

        lblKata.setText("Jumlah Kata: " + kata);
        lblKarakter.setText("Jumlah Karakter: " + karakter);
        lblKalimat.setText("Jumlah Kalimat: " + kalimat);
        lblParagraf.setText("Jumlah Paragraf: " + paragraf);
    }

